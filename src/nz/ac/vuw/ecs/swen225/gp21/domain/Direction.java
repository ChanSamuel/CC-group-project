/**
 * @author Benjamin
 * GameObjects have a direction
 */
public enum Direction {
	/**
	 * Object is facing north
	 */
	NORTH,
	/**
	 * Object is facing south
	 */
	SOUTH,
	/**
	 * Object is facing east
	 */
	EAST,
	/**
	 * Object is facing west
	 */
	WEST,
	/**
	 * Object has no direction
	 */
	NONE
}
